package com.example.ismobile;

public class JadwalDosen {
    String nama;
    String nim;
    String skripsi;
    String waktu;
    String kategori;
    int ava;

    public JadwalDosen(String kategori, String nama, String nim, String skripsi, String waktu) {
        this.nama = nama;
        this.nim = nim;
        this.ava = ava;
        this.waktu = waktu;
        this.skripsi = skripsi;
        this.kategori = kategori;
    }
}

